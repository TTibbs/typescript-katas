export const addNumbers = (num1: number, num2: number): number => {
  return num1 + num2;
};

export const subtractNumbers = (num1: number, num2: number): number => {
  return num1 - num2;
};

export const multiplyNumbers = (num1: number, num2: number): number => {
  return num1 * num2;
};

export const divideNumbers = (num1: number, num2: number): number => {
  return num1 / num2;
};
